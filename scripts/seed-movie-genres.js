let updated = 0
let skipped = 0
let missing = []

for (const folder of fs.readdirSync(MOVIES_DIR)) {
  const dir = path.join(MOVIES_DIR, folder)
  if (!fs.statSync(dir).isDirectory()) continue
  const sidecarPath = path.join(dir, 'movie.json')
  const genres = GENRES[folder]
  if (!genres) {
    missing.push(folder)
    continue
  }
  let sidecar = {}
  if (fs.existsSync(sidecarPath)) {
    try { sidecar = JSON.parse(fs.readFileSync(sidecarPath, 'utf-8')) } catch { sidecar = {} }
  }
  sidecar.genre = genres
  fs.writeFileSync(sidecarPath, JSON.stringify(sidecar, null, 2), 'utf-8')
  updated++
}

console.log(`Updated: ${updated}`)
console.log(`Skipped (no mapping): ${missing.length}`)
if (missing.length) console.log(missing.map((m) => '  - ' + m).join('\n'))
